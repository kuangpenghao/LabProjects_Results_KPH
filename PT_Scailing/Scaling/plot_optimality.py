# 计算 D 轴和 Loss 上升百分比轴
distances = []
loss_increases = []

for row in data:
    loss_prime = row[0]
    S_prime = np.array(row[1:])
    d = np.sqrt(np.sum(((S_prime - S_base) / S_base) ** 2))
    distances.append(d)
